# 1. print()
# ----------------------------------------
# - Used to display output on the screen.
# - Can print strings, numbers, variables, and expressions.
# - Multiple values can be printed using commas.

# Example:
# print("Hello, World!")
# print(name)
# print(10 + 5)

# ----------------------------------------
# 2. Comments
# ----------------------------------------
# - Comments improve code readability.
# - Single-line comments start with '#'.
# - Multi-line comments use triple quotes (''' or """).

# Example:
# # This is a single-line comment

# """
# This is a
# multi-line comment.
# """

# ----------------------------------------
# 3. Variables
# ----------------------------------------
# - Variables are used to store data.
# - Assignment is done using '='.
# - Variable values can be updated anytime.

# Example:
# name = "Prasun"
# age = 29

# ----------------------------------------
# 4. Data Types
# ----------------------------------------
# Common Python data types:

# int     -> Integer numbers
# float   -> Decimal numbers
# str     -> Text/String
# bool    -> True or False

# Examples:
# age = 29
# height = 166.5
# name = "Prasun"
# is_learning = True

# ----------------------------------------
# 5. type()
# ----------------------------------------
# - Returns the data type of a variable or value.

# Example:
# print(type(age))
# print(type(name))

# ----------------------------------------
# 6. Variable Naming Rules
# ----------------------------------------
# ✔ Must start with a letter or underscore.
# ✔ Cannot start with a number.
# ✔ Cannot contain spaces.
# ✔ Case-sensitive.
# ✔ Avoid using Python keywords.
# ✔ Use meaningful variable names.

# Good Examples:
# student_name
# total_marks
# is_active

# Bad Examples:
# 2name
# my name
# class

# ----------------------------------------
# 7. Python Keywords
# ----------------------------------------
# - Keywords are reserved words with predefined meanings.
# - They cannot be used as variable names.

# Examples:
# if
# else
# for
# while
# True
# False
# class
# def
# return
# import

# ----------------------------------------
# Key Takeaways
# ----------------------------------------
# ✔ Python is case-sensitive.
# ✔ Variables can store different data types.
# ✔ Use meaningful variable names.
# ✔ print() displays output.
# ✔ type() identifies data types.
# ✔ Comments improve readability.
# ✔ Keywords cannot be used as variable names.

